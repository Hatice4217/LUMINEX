const setLanguage = (lang) => {
    localStorage.setItem('language', lang);
    updateTexts(lang);
    updateSelectedLang(lang);
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
};

const updateTexts = (lang, rootElement = document) => {
    // Update elements with data-lang attribute
    rootElement.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.dataset.lang;
        if (translations[lang] && translations[lang][key]) {
            // Check if translation contains HTML (has < or >)
            const translation = translations[lang][key];
            if (translation.includes('<')) {
                // Use innerHTML for translations with HTML content
                el.innerHTML = translation;
            } else {
                // Use textContent for plain text
                el.textContent = translation;
            }
        }
    });

    // Update input/textarea placeholders with data-placeholder attribute
    rootElement.querySelectorAll('[data-placeholder]').forEach(el => {
        const key = el.dataset.placeholder;
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update select options with data-lang attribute
    rootElement.querySelectorAll('select option[data-lang]').forEach(el => {
        const key = el.dataset.lang;
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Legacy support for data-lang-placeholder
    rootElement.querySelectorAll('[data-lang-placeholder]').forEach(el => {
        const key = el.dataset.langPlaceholder;
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
};

const updateSelectedLang = (lang) => {
    const langData = {
        tr: { flag: 'tr', name: 'TÃ¼rkÃ§e' },
        en: { flag: 'gb', name: 'English' }
    };

    if (langData[lang]) {
        document.querySelectorAll('.selected-lang').forEach(selectedLangElement => {
            const img = selectedLangElement.querySelector('img');
            const span = selectedLangElement.querySelector('span');
            
            if (img) img.src = `https://flagcdn.com/${langData[lang].flag}.svg`;
            if (span) span.textContent = langData[lang].name;
        });
    }
};

const initLanguageSwitcher = () => {
    const langSwitchers = document.querySelectorAll('.lang-switcher');
    if (langSwitchers.length === 0) {
        return;
    }

    langSwitchers.forEach(langSwitcher => {
        const selectedLang = langSwitcher.querySelector('.selected-lang');
        const langOptions = langSwitcher.querySelector('.lang-options');

        if (selectedLang && langOptions) {
            // Remove old listeners by cloning
            const newSelectedLang = selectedLang.cloneNode(true);
            selectedLang.parentNode.replaceChild(newSelectedLang, selectedLang);

            newSelectedLang.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();

                langOptions.classList.toggle('show-lang-options');
                langSwitcher.classList.toggle('active');

                // Close all other lang options and remove active class
                document.querySelectorAll('.lang-switcher').forEach(ls => {
                    if (ls !== langSwitcher) {
                        ls.classList.remove('active');
                        const otherOptions = ls.querySelector('.lang-options');
                        if (otherOptions) otherOptions.classList.remove('show-lang-options');
                    }
                });
            });

            langOptions.querySelectorAll('li').forEach(li => {
                li.addEventListener('click', (e) => {
                    e.stopPropagation();
                    const lang = li.dataset.lang;
                    if (lang) {
                        setLanguage(lang);
                        langOptions.classList.remove('show-lang-options');
                        langSwitcher.classList.remove('active');
                    }
                });
            });
        }
    });

    // Close all when clicking anywhere else
    document.addEventListener('click', (e) => {
        if (!e.target.closest('.lang-switcher')) {
            document.querySelectorAll('.lang-switcher').forEach(ls => {
                ls.classList.remove('active');
                const opt = ls.querySelector('.lang-options');
                if (opt) opt.classList.remove('show-lang-options');
            });
        }
    });
};

document.addEventListener('DOMContentLoaded', () => {
    initLanguageSwitcher();
    // Initial language setting on page load
    const savedLang = localStorage.getItem('language') || 'tr';
    setLanguage(savedLang);
});

